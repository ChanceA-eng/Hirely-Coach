#!/usr/bin/env node
/**
 * generate-audio-map.mjs
 * Generates app/data/audioMap.ts from all lesson JSON files.
 * Run: node scripts/generate-audio-map.mjs
 */

import { readFileSync, readdirSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const lessonDir = join(root, "app/data/lessons");
const outFile = join(root, "app/data/audioMap.ts");

// Which JSON file belongs to which module number
const MODULE_ASSIGNMENT = {
  "module-1-phonics.json": 1,
  "module-2-grammar.json": 2,
  "module-3-vocabulary.json": 3,
  "module-4-conversation.json": 4,
  "module-4-pronouns-verbs.json": 4,
  "module-5-food-shopping.json": 5,
  "module-5-vocabulary.json": 5,
  "module-6-conversation.json": 6,
  "module-6-vocabulary.json": 6,
  "module-7-conversation.json": 7,
  "module-7-interview.json": 7,
  "module-8-interview.json": 8,
  "module-8-weather-feelings.json": 8,
  "module-9-directions-community.json": 9,
  "module-10-introducing-yourself.json": 10,
  "module-11-interview.json": 11,
  "module-12-exit-exam.json": 12,
  // source-of-truth files — assign to their primary module
  "alphabet-source-of-truth.json": 1,
  "numbers-colors-source-of-truth.json": 2,
  "pronouns-audio-schema.json": 4,
  "pronouns-verbs-source-of-truth.json": 4,
  "sounds-source-of-truth.json": 1,
};

function extractAudioRefs(obj, refs) {
  if (!obj || typeof obj !== "object") return;
  if (Array.isArray(obj)) {
    obj.forEach((i) => extractAudioRefs(i, refs));
    return;
  }
  for (const [k, v] of Object.entries(obj)) {
    if (
      typeof v === "string" &&
      k.toLowerCase().includes("audio") &&
      v.includes("/audio/")
    ) {
      const stem = v.replace("/audio/", "").replace(/\.mp3$/i, "");
      refs.add(stem);
    } else if (typeof v === "object") {
      extractAudioRefs(v, refs);
    }
  }
}

// Collect stems per module
const byModule = {};
const files = readdirSync(lessonDir).filter((f) => f.endsWith(".json"));

for (const f of files) {
  const mod = MODULE_ASSIGNMENT[f];
  if (!mod) {
    console.warn(`⚠  Skipping unassigned file: ${f}`);
    continue;
  }
  const data = JSON.parse(readFileSync(join(lessonDir, f), "utf8"));
  const refs = new Set();
  extractAudioRefs(data, refs);
  if (!byModule[mod]) byModule[mod] = new Set();
  refs.forEach((r) => byModule[mod].add(r));
}

// Build TypeScript output
const lines = [
  `// AUTO-GENERATED by scripts/generate-audio-map.mjs — do not edit manually.`,
  `// Each key is module-prefixed: m{N}_{stem}. Values are filenames in /public/audio/.`,
  `// To regenerate: node scripts/generate-audio-map.mjs`,
  ``,
  `export const audioMap: Record<string, string> = {`,
];

for (const mod of Object.keys(byModule).sort((a, b) => +a - +b)) {
  lines.push(``);
  lines.push(
    `  // ── Module ${mod} ${"─".repeat(Math.max(0, 60 - `Module ${mod} `.length))}`
  );
  const stems = [...byModule[mod]].sort();
  for (const stem of stems) {
    const key = `m${mod}_${stem}`;
    lines.push(`  ${key}: "${stem}.mp3",`);
  }
}

lines.push(`};`);
lines.push(``);
lines.push(
  `/** Flat set of every unique audio filename referenced across all modules. */`
);
lines.push(
  `export const allAudioFiles: ReadonlySet<string> = new Set(Object.values(audioMap));`
);
lines.push(``);
lines.push(
  `/** Reverse lookup: filename → array of keys that reference it. */`
);
lines.push(`export function keysForFile(filename: string): string[] {`);
lines.push(`  return Object.entries(audioMap)`);
lines.push(
  `    .filter(([, v]) => v === filename)`
);
lines.push(`    .map(([k]) => k);`);
lines.push(`}`);
lines.push(``);

const output = lines.join("\n");
writeFileSync(outFile, output, "utf8");

// Summary
const totalKeys = Object.values(byModule).reduce(
  (acc, s) => acc + s.size,
  0
);
console.log(`✅ Generated ${outFile}`);
console.log(`   ${totalKeys} audio keys across ${Object.keys(byModule).length} modules`);
for (const [mod, set] of Object.entries(byModule).sort(
  (a, b) => +a[0] - +b[0]
)) {
  console.log(`   Module ${mod}: ${set.size} keys`);
}
